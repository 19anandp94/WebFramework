<h1>Scavix WDF Documentor</h1>
<p>
	This is a tool to create documentation from DocComments in the scavix-wdf sourcecode.
	We use it to automate and speed up things - and it will constantly remind us of missing documentation.
	It will create a bunch of cross-linked markdown files that we push to the scvavix-wdf.wiki repository
	thus using the standard GitHub mechanism for our documentation.
</p>
